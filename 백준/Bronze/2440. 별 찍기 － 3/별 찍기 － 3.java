import java.util.*;
public class Main {
  public static void main (String[] args) {
    Scanner console = new Scanner (System.in);
    int s=console.nextInt();
    for (int i=s; i>=1; i--){
      for(int j=i; j>=1; j--){
        System.out.print("*");
      }
      System.out.println(" ");
      }
    }
    }